  1,03</p>

  <p align="justify">Средняя стоимость
  квадратного метра на май 2011 года в
  городе Владивостоке составила 70 000 рублей. Считаем:
  70 000 х 0,94 х 0,98 х 0,97 х 0,98
  х1,03 х 50 кв. метров = 3 156 873рубля.</p>

  <p align="justify">Так же следует
  учесть, что средняя стоимость квадратного метра рассчитывается с
  цены предложения квартир, реальная продажная стоимость квартир
  может быть меньше, поэтому для объективной оценки следует
  помнить, что в расчетную цену входит запас на торг, с покупателем
  (особенно это актуально при снижении стоимости жилья),
  всевозможные накладные расходы и агентские вознаграждения.</p>

  <p align="justify">Умение оценить
  мотивацию владельца жилья совершить продажу в данный момент
  времени с учетом текущей рыночной ситуации определит вероятность
  получения комиссионных. Для оценки этого факта выясните
  следующее.</p>

  <p align="justify">-Нацелен ли хозяин
  жилья на то, чтобы непременно найти покупателя по непомерно
  раздутой им цене?</p>

  <p align="justify">-Что для хозяина
  более важно: продать объект по назначенной им цене или вообще
  <i><b>продать</b></i> его?</p>

  <p align="justify">-Готов ли хозяин
  обсуждать реальную рыночную цену своего жилья?</p>

  <p align="justify">Добывая ответы на
  эти вопросы, Вы на самом деле пытаетесь выяснить,
  <i><b>нужно</b></i> ли хозяину продать жилье или он просто
  <i><b>хочет</b></i> его продать. Те, у кого действительно есть
  необходимость совершения продажи, с большей степенью вероятности
  станут Вашими потенциальными клиентами и вероятность совершения
  продажи и получения Вами комиссионных в этом случае выше, чем с
  теми хозяевами, которые просто хотят протестировать ситуацию на
  рынке жилья. Необходимость совершения продажи, как правило,
  возникает у людей, которые: - Переезжают в другой город или
  страну;</p>

  <p align="justify">- Испытывают
  финансовые затруднения;</p>

  <p align="justify">- Ожидают рождения
  ребенка и живут при этом в слишком маленькой квартире или доме;</p>

  <p align="justify">- Собираются разводиться;</p>

  <p align="justify">Несмотря на то, что
  обстоятельства, побудившие их совершать продажу, не всегда бывают
  приятными, для агента по недвижимости возникает ситуация, которая
  помогает добиться успешного результата.</p>

  <p align="justify">- Найдите время для
  того, чтобы задать вопросы и получить ответы позволяющие выяснить
  жизненную ситуацию клиента ;</p>

  <p align="justify">- Не прекращайте задавать вопросы. Если Вам не удалось получить на них ответы, но
  Вы почувствуете что у клиента есть побудительный мотив, попросите
  разрешения на предварительный просмотр жилья. При личной встрече
  у Вас появится больше шансов, да и просмотр не будет лишним.</p>

  <p><font color="#4F81BD"><b>ЗВОНОК ПРОДАВЦУ. ЧТО И КАК
  ГОВОРИТЬ.</b></font></p>

  <p align="justify">Когда Вы впервые
  звоните владельцу жилья, у которого истек срок выставления на
  продажу – поставьте перед собой единственную цель - <b>назначить
  время и место личной встречи.</b></p>

  <p align="justify">В процессе
  разговора: <b>«Расставьте точки над «и»:</b></p>

  <p align="justify">- В результате
  разговора убедите владельца, что в результате его сотрудничества
  с Вами результат будет иным, чем при работе с его предыдущим
  агентом.</p>

  <p align="justify">-Объясните, почему
  сотрудничество с Вами повышает вероятность продажи.</p>

  <p align="justify"><b>Действуйте с
  упреждением.</b> Продавцы обязательно выставят свое имущество на
  продажу через несколько дней после истечения срока действия
  предыдущего договора. Чтобы получить такой объект в свое
  распоряжение не следует медлить с действиями и мямлить при
  разговоре. Владельцы жилья готовы к дальнейшим действиям. ВЫ
  должны заставить поверить их в силу своих возможностей и убедить
  в своем умении успешно продавать недвижимость.</p>

  <p align="justify"><b>Оставьте себе
  возможность для маневра.</b> На данном этапе Вы можете не знать
  обо всех обстоятельствах, воспрепятствовавших продаже жилья в
  течение предыдущего срока. Вы не знаете состояния жилья , соседей
  , уровень доступа , который хозяин гарантирует покупателям,
  желаемой цены и временных рамок, вероятности удовлетворения
  ожиданий хозяина и действий , которые совершал предыдущий агент в
  течение срока действия договора.</p>

  <p align="justify">Поскольку множество
  факторов пока Вам не известны, оставьте себе возможность для
  маневра и не связывайте себя обещаниями, которые Вы, возможно, не
  сможете выполнить в будущем. Не называйте сразу и размера
  желаемых комиссионных. Необходимо гибко подходить к ситуации и
  действовать из требования рынка и мотивации потенциального
  клиента.</p>

  <p align="justify"><b>Обратите
  большинство часто задаваемых вопросов себе на пользу.</b> На
  вопросы типа « А что Вы будете делать иначе?» и «Почему мое жилье
  не продается?» ответьте, что пока Вы не обладаете достаточным
  количеством информации, чтобы дать точный ответ. Можно задать
  встречный вопрос, например: « Вы хотите, чтобы я угадал или
  предпочитаете знать наверняка?». Когда хозяин ответит, что хочет
  знать наверняка, назначьте личную встречу с ним для того, чтобы
  осмотреть жилье и провести дружескую беседу.</p>

  <p align="justify"><b>Соберите
  информацию.</b> Владелец должен четко понимать , что не обладая
  достоверной информацией о его объекте и ситуации , Вы не можете
  определить каким путем следует идти к достижению желаемого
  результата . Вам необходимо увидеть жилье для того, чтобы
  определить его состояние , а так же достоинства и недостатки.</p>

  <p align="justify">Необходимо
  выяснить, каким способом действовал предыдущий агент. Спросите
  владельца, что делал предыдущий агент для продажи жилья, если
  есть возможность, ознакомьтесь с рекламными материалами,
  размещенными предыдущим агентом, в том числе и в Интернете. И
  наконец, необходимо добиться взаимопонимания с владельцем,
  касаемо временных рамок, объявляемой цены, цены продажи и доступа
  потенциальных покупателей, владелец должен знать, с какой
  конкурентной ситуацией он столкнется при продаже своего
  жилья.</p>

  <p align="justify"><b>Подчеркните свои
  достоинства.</b> Чтобы добиться доверия владельца, ознакомьте его
  со своими предыдущими достижениями, или с успешной историей своей
  компании.</p>

  <p><font color="#4F81BD"><b>УМЕНИЕ ПРОДАВАТЬ</b></font></p>

  <p align="justify">Приобретение
  объекта недвижимости, у которого истек срок выставления на
  продажу в другом агентстве недвижимости, является результатом
  прекрасных навыков продаж, в том числе:</p>

  <p align="justify"><i>- Ежедневного
  поиска потенциальных клиентов;</i></p>

  <p align="justify" >
  <i>- Акцентированного диалога;</i></p>

  <p align="justify"><i>- Умения
  убеждать;</i></p>

  <p align="justify"><i>- Умения
  преодолевать препятствия;</i></p>

  <p align="justify"><i>- Донесение до
  владельцев преимуществ, которое дает сотрудничество с
  Вами;</i></p>

  <p align="justify">- Умения назначать
  встречи, которые приводя к результату;</p>

  <p align="justify">При установлении
  отношений с владельцами жилья, которое не удалость продать
  другому агенту, не стремитесь сразу же убедить его в
  необходимости повторного выставления на продажу с Вашей помощью.
  Ваша задача заключается в том, чтобы раззадорить владельца и
  привести убедительные аргументы в пользу сотрудничества с Вами. В
  не зависимости от сценария разговора запомните следующее. Не
  дайте загнать себя в тупик. Добивайтесь выполнения единственной
  задачи, которая заключается в назначении личной встречи. Наметьте
  круг потенциальных клиентов, которых Вы стремитесь превратить в
  своих потенциальных клиентов.</p>

  <p align="justify"><b>Руководствуйтесь
  такими критериями</b></p>

  <p align="justify">- Четкий
  побудительный мотив продажи ;</p>

  <p align="justify">- Каковы временные
  рамки совершения продажи ;</p>

  <p align="justify">- Определенное
  место в котором должен оказаться владелец к определенному
  сроку;</p>

  <p align="justify">- Возможность
  продажи по реальной цене с учетом комиссионных;</p>

  <p align="justify">- Престижное жилье
  в престижном районе;</p>

  <p align="justify">- Владелец, у
  которого нет родственника или друга работающего агентом по
  недвижимости;</p>

  <p align="justify" >Чтобы узнать,
  соответствуют ли владельцы недвижимости указанным выше критериям,
  им необходимо задать соответствующие вопросы. После этого Вы
  поймете, стоит ли тратить время на данный объект.</p>

  <p align="justify">Работая с
  владельцами жилья, которые с высокой степенью вероятности могут
  стать Вашими клиентами, звоните им по телефону и назначайте им
  встречи как минимум раз в две недели. Выясняйте, как продвигается
  процесс продажи, как часто приходят просматривать жилье
  потенциальные покупатели и самое важное – удалось ли продать
  жилье. Некоторым владельцам удается продать жилье самостоятельно,
  но необходимо помнить, что есть разница между понятием «продажа»
  и «закрытие сделки». Тот факт, что владельцу удалось договориться
  с покупателем о продаже, не означает автоматического получения
  денег. Как правило, покупатели ищут не владельцев, которые
  продают жилье без услуг агентов, а жилье, которые они хотят
  купить. Рынок насыщен предложениями и всегда можно
  переориентироваться на покупку другого объекта. Поэтому не
  торопитесь для себя «хоронить» этот объект. Постарайтесь
  сохранить контакт.</p>

  <p align="justify"><b>Почему агенты по
  недвижимости терпят неудачу?</b></p>

  <p align="justify">Причина, по которой
  агентам, работающим с «просроченными» объектами и с владельцами
  непроданной недвижимости удается добиться успеха, заключается в
  том, что большинство агентов не пытаются составить им
  конкуренцию, а даже если они и работают в этих направлениях, то
  делают это от случая к случаю и, как правило, выходят из игры
  задолго до совершения сделки. Если Вы хотите добиться успеха в
  области продаж, то должны быть готовы к тому, что слово «нет» Вам
  придется слышать гораздо чаще, чем слово « да». Статистика:</p>

  <p align="justify">- 44 % агентов
  прекращают работать с потенциальным клиентом после первой же
  ситуации, когда он говорит слово «нет». Иными словами, в первой
  же точке сопротивления почти половина агентов прекращает бороться
  за то, чтобы добиться продажи и получить свои комиссионные.</p>

  <p align="justify">- 22 % агентов
  прекращают работать с потенциальным клиентом после его второго
  отказа, это означает, что две трети агентов отказываются от
  получения денег после двух небольших проблем.</p>

  <p align="justify">- 14 % агентов
  прекращают работать с потенциальным клиентом после его третьего
  отказа.</p>

  <p align="justify">- 12 % агентов
  отправляются восвояси после четвертого отказа. Получается, что 92
  % агентов выходят из игры после четырех попыток совершения
  продажи. Это означает , что только 8 % всех агентов продолжают
  работать после четырех отказов. Исследования показали, что более
  60% продаж совершаются после того, как потенциальный клиент
  сказал «нет» как минимум четыре раза. Клиент может отказать из-за
  того, что:</p>

  <p align="justify">- ему не подходят
  сроки и условия сделки;</p>

  <p align="justify">-он не обладает
  полной информацией и ясностью ситуации;</p>

  <p align="justify">-текущие
  обстоятельства не совершают ему совершить сделку;</p>

  <p align="justify" >В любом случае 92 %
  агентов отказываются от дальнейших действий к тому моменту, когда
  продавец будет готов сказать «да». Это означает , что 8 % агентов
  контролируют 60 % рынка только потому, что настойчивы и ставили
  вопрос о продаже в тот момент , когда продавец был к этому
  готов.</p>


  <p align="justify"><font color="#4F81BD"><b>ЗАВОЕВАНИЕ КЛИЕНТУРЫ</b></font></p>

  <p align="justify" >Представьте себе,
  что Вас попросили написать вопрос, ответ на который гласит:</p>

  <p align="justify" ><b>«Функция,
  составляющая или разрушающая возможность успеха в качестве агента
  по недвижимости».</b> Дали несколько секунд на размышление. Что
  бы Вы написали? ……</p>

  <p align="justify">Правильный вариант
  вопроса звучит так<b>: «Что такое создание клиентуры?»</b></p>

  <p align="justify"><b>Агенты как
  неизбежное зло. Точка зрения, которая появляется и исчезает.</b>
  Точка зрения о том, что агентам переплачивают и они являются
  ненужным звеном в сделках с недвижимостью , является среди
  потребителей широко распространенной. Такая мысль моментально
  приходит в голову, когда устойчивый рынок обуславливает малое
  количество непроданных объектов, и продажа осуществляется очень
  быстро при наличии нескольких предложений в течение короткого
  времени после подаче объявлений об их продаже. В периоды
  экономического процветания сегмент потребителей и новых
  домовладельцев начинает ставить под сомнение целесообразность
  услуг агентов по недвижимости, особенно принимая во внимание их
  высокие гонорары. Необходимо запомнить, что в хорошие времена
  продается так много объектов недвижимости, что даже точка зрения
  о ненужности агентов, не ограничивает возможность их работы.
  Кроме того периоды процветания не бывают вечными. Когда рынок
  качнется в сторону нейтрального состояния, можно быть уверенным в
  увеличении конкуренции среди продавцов, увеличении количества
  непроданных объектов, и длительности их предложения на рынке.
  Тогда продавцы, в том числе и домовладельцы снова начнут с
  почтением относиться к агентам по недвижимости.</p>

  <p align="justify"><b>Почему создание
  клиентуры важнее обслуживания клиентов.</b> Прежде чем возразить
  дочитайте до конца. Вы должны преуспеть и в сфере
  наработки клиентуры и в сфере ее обслуживания. Однако приоритет
  следует отдавать именно созданию клиентуры.</p>

  <p align="justify"><b>- Нельзя
  заниматься обслуживанием клиентов, если их нет.</b>
  Непревзойденное мастерство обслуживания клиентов является
  результатом опыта работы в данной сфере, а создание клиентской
  базы является предпосылкой для достижения выдающихся результатов
  по обслуживанию.</p>

  <p align="justify">- <b>Большинству
  потребителей оказывают услуги столь плохого качества, что они не
  ожидают ничего хорошего.</b> Когда лица, предоставляющие те или
  иные услуги, делают то, что они обязаны сделать в определенный
  промежуток времени потребители удовлетворены тем, что получают.
  Естественно Вам необходимо приобрести опыт, позволяющий оказывать
  услуги исключительного качества, но даже если Вы просто будете
  придерживаться взятых на себя обязательств, то уровень
  предоставляемых Вами услуг будет выше, чем у большинства
  конкурентов.</p>

  <p align="justify"><b>- Создание
  клиентуры является более сложной задачей, чем процесс
  предоставления услуг.</b> Наработка клиентуры требует умения
  продавать и постоянного настойчивого поиска клиентов. Для
  создания клиентуры необходимо достичь такого уровня навыков и
  позитивного опыта, которых будет достаточно для того, чтобы
  впоследствии просто снять телефонную трубку, позвонить людям,
  которые Вам знакомы и выяснить возможность заключения сделки.</p>

  <p align="justify">Умение продавать основывается на том,
  насколько эффективно Вы создаете планы на будущее и следуете им
  при получении заказов, как оцениваете объекты, проводите встречи,
  заключаете эксклюзивные договора и оказываете услуги недавно
  приобретенному клиенту.</p>

  <p align="justify">В условиях стабильного рынка агенту
  достаточно просто вести свои дела. Но когда спрос начинает падать
  , успех на поприще недвижимости не приходит автоматически. Только
  превосходное умение продавать гарантирует, что именно Вы, а не
  другой агент завоюете клиента не зависимо от состояния рынка.
  Самые успешные агенты зарабатывают именно в период нестабильной
  рыночной ситуации.</p>

  <p align="justify">Вне зависимости от экономической
  ситуации на рынке, имеются покупатели и продавцы недвижимости.
  Вне зависимости от темпов развития экономики у людей всегда есть
  необходимость или желание изменить условия своего проживания.
  Рождаются дети. Люди переезжают. Молодые люди вступают в брак.
  Многие разводятся. Поэтому на рынке недвижимости большие шансы
  добиться успеха имеют те агенты , которые обладают лучшими , по
  сравнению с остальными , навыками продаж. <font color="#4F81BD"><i><b>Чтобы защитить себя от воздействия изменяющейся ситуации на
  рынке необходимо вооружится навыком поиска потенциальных
  клиентов.</b></i></font></p>

