  сотрудничества с ними. Еще одним важным моментом является
  «видение успеха». Поиск потенциальных клиентов , требует
  позитивных ожиданий, позитивного склада мышления, умение
  преодолевать влияние агентов , которые недооценивают важность
  работы на перспективу и , тем самым негативно влияют на вашу
  точку зрения и ожидание успеха.</p>

