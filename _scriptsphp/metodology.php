  <p style="margin-left: 4.25cm; text-indent: -3cm;">
  <font style="font-size: 16pt"><b>НАВЫКИ И УМЕНИЯ
  УСПЕШНОГО АГЕНТА ПО НЕДВИЖИМОСТИ.</b></font></p>

  <p>Для каждого агента
  по недвижимости успех означает что-то свое. Однако для достижения
  успеха необходимо знать основы, вне зависимости от того, что
  побуждает Вас заниматься этой профессией. Агенты, которые могут
  считать себя успешными, обладают четырьмя общими
  качествами:</p>

  <p><b>- Последовательность.</b> Каждый день
  агенты совершают действия, способствующие достижению успеха.
  Работа рывками, когда за 2 дня человек делает 50 телефонных
  звонков, а потом 2 недели отдыхает, тут не годится. Для
  достижения цели нужно работать методично и постоянно, изо дня в
  день.</p>

  <p><b>- Вера в закон накопления.</b> Закон
  накопления – это принцип, который гласит, что постоянные усилия,
  как негативные, так и позитивные, в конце концов, приносят
  результат. Ни одному агенту не удавалось еще добиться мгновенного
  успеха, но целеустремленные, ориентированные на достижения
  результата действия накапливаются и становятся толчком, всякий
  раз приводящим к общему позитивному результату.</p>

  <p><b>- Век живи, век учись.</b> Наиболее
  успешные агенты никогда не прекращают совершенствоваться. Тяга к
  совершенствованию у них чрезвычайна сильна, и поэтому такие
  агенты тратят время, ресурсы и энергию для того , чтобы постоянно
  повышать свою квалификацию и продуктивность работы.</p>

  <p><b>- Самодисциплина.</b> Умение
  мотивировать свои действия и выполнять работу, которую необходимо
  сделать. Успешный агент работает целый день и совершает
  высокопродуктивные действия, в том числе ищет потенциальных
  клиентов и доводит работу до конца. Такой человек заставит себя
  делать работу, которую выполнять не хочется, чтобы в будущем
  получить желаемый результат.</p>

  <p><font color="#4F81BD"><i>Агенты по недвижимости являются доверенными
  представителями и финансовыми консультантами, а не людьми,
  которым платят за то, что они открывают двери домов перед
  потенциальными покупателями.</i></font></p>

  <p>Доверенное лицо – это человек,
  которого наняли для представления интересов другого (физического
  или юридического лица). Между доверенным лицом и заказчиком
  сделки устанавливаются особые отношения обязательств,
  эксклюзивности представления интересов этики и
  конфиденциальности.</p>

  <p><b>- Определение задач клиента.</b>
  Чтобы быть хорошим доверенным лицом необходимо прежде всего четко
  понимать, какие задачи преследует Ваш клиент , продавая или
  покупая имущество. Слишком многое агенты сталкивались с
  проблемами из-за того, что не имели определенных знаний об
  интересах клиентов, которых представляли.</p>

  <p><b>- Оказание консультации.</b>
  Подобно адвокатам, консультирующим клиентов вы должны направить
  своего клиента в сторону принятия верного решения. Это касается
  оценки стоимости недвижимости, стратегии формирования цены,
  маркетинга и составления контракта таким образом, чтобы клиент
  получил максимальную финансовую выгоду. Многие агенты
  сталкиваются с проблемами из боязни донести до клиента правдивую
  информацию. Этого делать нельзя. Вам придется приносить своим
  клиентам как хорошие, так и плохие новости. Рыночные условия
  могут меняться, и стоимость объекта придется снижать или
  увеличивать. В подобных условиях решающим фактором является Ваше
  спокойствие, способное указывать пути решения проблем. И наличие
  тесных взаимосвязей с клиентом.</p>

  <p><b>КАК НЕ ПРЕВРАТИТЬСЯ В КЛЮЧ ДЛЯ
  ДОСТУПА К ОБЪЕКТАМ НЕДВИЖИМОСТИ.</b></p>

  <p>В то время, когда Интернет еще не
  получил столь широкого распространения единственный путь клиента
  к информации о недвижимости, выставленной на продажу лежал через
  агентство. Агенты получали целые тома информации о недвижимости с
  ее описанием и зачастую черно- белой фотографией. Сегодня
  потенциальный клиент может не приезжать в агентство по
  недвижимости, а найти интересующий объект через Интернет. Однако
  когда клиент найдет интересующий его объект, ему придется
  обратиться либо к владельцу, либо к агенту, для того, чтобы
  получить разрешение на осмотр объекта. Именно здесь и начинаются
  маленькие хитрости.</p>

  <p>Зачастую клиент обращается к
  агенту, считая его чем- то вроде ключа, открывающего доступ к
  объекту недвижимости. <font color="#4F81BD"><i>Будучи
  агентом, вам необходимо продемонстрировать определенные навыки
  для того, чтобы оценить уровень заинтересованности клиента и его
  умение покупать, а затем превратить обычный запрос в
  обязательство совершения сделки.</i></font></p>

  <p><font color="#4F81BD"><i><b>ДЕСЯТЬ СОВЕТОВ ПО РАБОТЕ С
  ПОКУПАТЕЛЯМИ</b></i></font></p>

  <p><b>- Оцените потенциал клиента.</b>
  Потенциальный клиент либо имеет побудительный мотив к совершению
  сделки, либо не имеет его. Агенты, работающие с клиентами,
  которые обладают слабой мотивацией, и стремящиеся повысить ее и
  убедить покупателя предпринять действия, направленные на
  заключение сделки, напрасно теряют время. Агент может сообщить о
  том , какие преимущества получит клиент в результате совершения
  тех или иных действий, какая организация или банк предлагает
  самые привлекательные проценты по ипотечным кредитам,
  проинформировать о текущей рыночной ситуации, но он не может
  привить ему необходимости немедленно приступать к делу.</p>

  <p>Потенциальный клиент должен иметь
  <font color="#4F81BD"><i><b>мотивацию
  для совершения покупки, понимать необходимость совершения
  определенных действий в короткий промежуток времени и чувствовать
  себя обязанным предоставить Вам эксклюзивные права
  сотрудничества</b></i></font>. Если у Вашего клиента эти
  качества отсутствуют лучше отказаться от сотрудничества с ним и
  направить свои силы на поиски лучшего кандидата.</p>

  <p><b>- Работайте только с верными Вам
  клиентами.</b> Многие полагают, что прояснить ситуацию может
  ответ на вопрос о том обращался ли клиент к другим агентам.
  Данный вопрос поставлен неверно. Верным будет вопрос: «Связаны
  ли Вы обязательствами с другим агентом?» Он раскрывает мысль,
  которую Вы хотите привить своему клиенту, а именно: необходимость
  связать себя обязательствами только с Вами. В результате
  выполнения своих обязательств по отношению к клиенту агент
  получает вознаграждение. Клиенты, которые только «сотрудничают»
  с Вами не дают никаких обещаний по поводу конечного результата.
  Это означает, что в любой момент клиент может «переметнуться» к
  другому агенту, а Вы останетесь ни с чем.</p>

  <p>Если Вы рекламируете объект
  недвижимости, и Вам звонит потенциальный покупатель, не
  предлагайте просто встретиться и посмотреть объект. Ваша задача
  заключается в том, чтобы убедить покупателя в необходимости
  сотрудничества с Вами. Пусть даже на короткое время. Если Вы
  чувствуете, что для установки прочных отношений необходимо
  продемонстрировать объект - сделайте это. Но обязательно
  подпишите с клиентом «Соглашение о просмотре». О том, что Вы
  будете подписывать такое соглашение, предупредите клиента заранее
  при назначении времени и места встречи. Соответственно у него
  должен быть с собой паспорт или любой документ удостоверяющий его
  личность. Клиент, настроенный на сотрудничество не станет
  возражать и препятствовать этому. Ведь он выразил готовность на
  сотрудничество. Клиент который будет этому всячески
  препятствовать и возражать вряд ли будет настроен на
  перспективную работу с Вами. Вы для него ключ для доступа к
  объекту. С вероятностью как минимум 30% он вернется после
  окончания просмотра для ведения переговоров с продавцом. Если Вы,
  как агент, не будете настаивать на таком уровне отношений, то
  рискуете впоследствии потерять покупателя без всякой компенсации
  своего труда.</p>

  <p><b>-Не предполагайте, что  покупатель верен Вам.</b> Конечно же, не стоит наивно полагать
  что покупатель, с которым вы подпишете «Соглашение о просмотре»
  даст 100% гарантию, что он не будет искать обходных путей в
  случае, если объект ему понравиться или наоборот останется только
  с Вами подбирать для приобретения в будущем другие объекты. Важно
  то, что подписанное ранее «Соглашение» является неким
  краткосрочным контрактом, который поможет наладить контакт на
  первом этапе, возможно, остановит покупателя от неправильных
  действий направленных на увод объекта. Вы получаете большее
  преимущество перед другими агентами. Вы всегда сможете напомнить
  о себе.</p>

  <p><b>- Добейтесь назначения
  встречи.</b> Путь к успеху начинается с личной встречи.
  Назначайте ее всегда в месте, которое в будущем позволит вам
  иметь возможность при желании отказать в просмотре объекта
  клиенту, не раскрыть его настоящего местонахождения. Все очень
  просто. Назначая встречу у подъезда, где располагается квартира,
  несложно, зная этаж и расположение объектов по количеству комнат
  вычислить квартиру. Агент другой компании представившись
  клиентом с удовольствием понаблюдает за Вами из окна своей машины, 
  как вы мерзните ожидая его. Описание Ваше он знает с ваших
  слов. А потом, увидев, что вы покидаете место встречи, поднимется
  на этаж и позвонит в дверь нужной квартиры с вопросом: «Извините,
  мне сказали, что Вы продаете квартиру. Можно взглянуть?»</p>

  <p>Назначайте встречу на углу дома, у
  другого подъезда, при описании объекта меняйте этажность. Если
  видите, что клиент очевидно по всем признакам похож на агента
  другой компании, или противится подписанию «Соглашения» и всем
  видом показывает пренебрежение к Вам не стоит открывать ему путь
  к вашему объекту.</p>

  <p><b>-Будьте готовы к типичным
  недоразумениям.</b> Покупатели, в особенности те, которые
  совершают покупку впервые (именно такой является клиентура
  начинающих агентов по недвижимости), зачастую, заключают сделку,
  обладая минимальными знаниями и недопониманием сути работы агента
  и процесса реализации сделок. Поэтому будьте готовы преодолевать
  типичные проблемы и возникающие в процессе общения с такими
  покупателями.</p>

  <p><font style="font-size: 12pt"><b>Многие покупатели
  не понимают, почему в процессе поиска и покупки жилья они должны
  эксклюзивно работать с каким-то одним агентом по
  недвижимости.</b></font> Многие покупатели считают, что если они
  нашли подходящее жилье, им вполне достаточно агента,
  представляющего интересы продавца. Известно, что агентские
  комиссионные выплачиваются продавцом, и поэтому покупатель
  считает для себя возможным «упасть на хвост», воспользовавшись
  опытом и временем агента продавца. Ваша задача заключается в том,
  чтобы помочь покупателю понять, что при сотрудничестве со своим
  собственным агентом, т.е. с Вами он получит такие же грамотные
  консультации и уважительное отношение к себе, как и, например,
  при сотрудничестве с адвокатом, которого он нанимает для
  представления своих интересов в суде.</p>

  <p><font style="font-size: 12pt"><b>Покупатель не
  понимает, что в начале поиска подходящего объекта недвижимости
  агент должен оценить его потенциал, а еще лучше получить
  подтверждение платежеспособности. </b></font>
  Объясните клиенту, что покупатель стремиться найти
  самое подходящее для себя жилье, а продавец – самого подходящего
  для себя покупателя. Если продавцу сделают два предложения, одно
  из которых будет от покупателя с подтвержденной
  платежеспособностью, а другое от покупателя, финансовая
  состоятельность которого вызывает множество вопросов, то не слоит
  долго гадать, какой вариант он предпочтет.</p>

  <p><font style="font-size: 12pt"><b>Слишком многие
  покупатели имеют какое-то устоявшееся представление о своем
  идеальном объекте недвижимости и считают, что если они будут
  долго и настойчиво его искать, а так же если подключат к этому
  поиску как можно больше агентов, то смогут найти иголку в стоге
  сена.</b></font> Истина же заключается в том, что покупатели не
  приобретают идеальный вариант, а своими силами превращают
  купленное жилье дом своей мечты. Если Ваш клиент мечтает
  приобрести готовое совершенство, его необходимо, либо заставить
  снять розовые очки, либо отказаться от сотрудничества. Перечень
  заблуждений покупателей можно составлять бесконечно. Большинство
  покупателей считают, что нельзя делать сразу самое высокое
  предложение, а лучше начинать с небольшой суммы и постепенно ее
  увеличивать. Хуже всего то, что большинство из них убеждено
  подобными мыслями постарайтесь донести до него простую истину.
  Чаще всего разочаровываются именно те покупатели, которые
  упустили возможность приобрести желаемый объект недвижимости
  из-за того, что не сделали предложение вовремя, или предложили
  продавцу такую сумму, которая не смогла конкурировать с другими
  вариантами.</p>

  <p><b>- Объясните, какие услуги Вы
  предоставляете. </b>Не стоит предполагать, что покупатели
  понимают причины и сущность ваших действий по реализации сделки.
  Они этого не понимают. Не знают они и того, что отличает Вас от
  других агентов по недвижимости. Детально опишите своему клиенту
  свои действия и укажите причины, по которым Вам удается превзойти
  других агентов.</p>

  <p><b>- Установите сотрудничество с
  кредитором.</b> Один из лучших способов добиться верности со
  стороны покупателя заключается в том, чтобы на начальном этапе
  сотрудничества познакомить его с кредитной организацией ,
  предоставляющей выгодные условия ипотечной ссуды . Такое
  партнерство дает два основных преимущества:</p>

  <p>- Кредитор может оценить финансовую
  состоятельность потенциального клиента. Защитит Вас от напрасной
  работы с финансово несостоятельным клиентом или от поиска
  объекта, не соответствующего финансовым возможностям
  покупателя.</p>

  <p>- Напротив, потенциального клиента,
  заслуживающего доверия кредитора Вы передаете квалифицированному
  специалисту. Не стоит забывать. Свою несостоятельность проявляют
  не только агенты по недвижимости, но и многие кредитные
  учреждения. Если Вы пустите выбор кредитной организации на
  самотек, то возрастает вероятность того , что сделка попадет в
  руки неизвестных Вам людей. Это заставит Вас прилагать больше
  усилий для того, чтобы довести сделку до логического
  завершения.</p>

  <p><b>Задайте потенциальному
  покупателю ряд вопросов.</b> Вопреки распространенному мнению
  агентов потенциальные клиенты вовсе не жаждут услышать пустые
  разглагольствования по поводу процесса купли- продажи. Они хотят
  говорить о <font color="#4F81BD"><i><b>своих желаниях, потребностях и
  выгодах. </b></i></font>Вооружитесь списком продуманных
  вопросов, позволяющих не только собрать информацию, необходимую
  для надлежащего выполнения своей работы, но и установить
  взаимопонимание и доверительные отношения, которые позволят
  добиться приверженности клиента.</p>

  <p><b>- Держите ситуацию под
  контролем.</b> При работе с продавцами недвижимости Вы либо
  заключаете договор о сотрудничестве, либо не делаете этого. Но
  при этом Вы включаете продаваемый объект в свой список. При
  установлении отношений с покупателем, Вы должны знать, будете ли
  представлять его интересы эксклюзивно. Работать с покупателем
  сложнее, чем с продавцом. Он может отказаться от эксклюзивного
  сотрудничества и предпочесть воспользоваться услугами нескольких
  агентов. Покупатель может изменить точку зрения о своих
  потребностях. Он может составить нереалистическую картину своих
  представлений о новом жилье. По этим причинам Вы должны иметь
  четкое представление о своих взаимоотношениях с покупателем и
  постоянно держать ситуацию под контролем. Первый шаг заключается
  в том, чтобы убедить покупателя о преимуществах сотрудничества с
  одним агентом. Затем необходимо выяснить финансовые возможности
  покупателя и его ожидания. Для этого ему необходимо задать
  вопросы, и по полученным ответам сделать для себя выводы. Третий
  очень важный шаг заключается в том, чтобы контролировать
  покупателя в процессе просмотра, его оценки, выбора. Кроме этого
  необходимо, чтобы все этапы реализации сделки проходили под Вашим
  чутким просмотром и руководством. В процессе работы с покупателем
  будьте готовы привести его точку зрения в соответствие жизненным
  реалиям.</p>

  <p><b>- Не бойтесь отказывать.</b> Если
  клиент от Вас хочет невозможного, откажите ему. Если Вы не
  можете встретиться с ним в субботу, скажите ему об этом. Если
  покупатель хочет, чтобы Вы сделали предложение, которое выставит
  Вас в неприглядном свете в глазах продавца и его агента,
  откажитесь это делать.</p>

  <p><font style="font-size: 16pt"><b>МЕТОДИКА ПО РАБОТЕ С «ПРОСРОЧЕННЫМИ»
  ОБЪЕКТАМИ И С ВЛАДЕЛЬЦАМИ НЕДВИЖИМОСТИ</b></font></p>

  <p align="justify"><font color=
  "#4F81BD"><i><b>- Три причины, по которым следует работать с «просроченными» объектами
  и хозяевами жилья;</b></i></font></p>

  <p align="justify"><font color="#4F81BD"><i><b>- Как
  превратить плохой объект в хороший. Основы работы с безуспешно
  продававшимися объектами;</b></i></font></p>

  <p align="justify"><font color="#4F81BD"><i><b>- Как
  войти в мир владельцев жилья, которые ранее предпочитали не
  пользоваться услугами агентов;</b></i></font></p>

  <p align="justify">Если
  Вы хотя бы немного поработали в сфере недвижимости, то наверняка
  заметили, что многие агенты с предубеждением относятся к тем
  объектам, которые ранее выставлялись на продажу и которые хозяева
  продают напрямую. Агенты считают, что объекты подобны подержанным
  товарам, продажа которых не заслуживает внимания и сил. Это
  мнение ОШИБОЧНО. Они сами отворачиваются от огромного
  потенциального дохода и теряют для себя огромное количество
  возможных продаж.</p>

  <p align="justify">- Несмотря на то, что данные
  объекты являются прекрасными источниками дохода они не должны
  быть единственным направлением деятельности. У агента должно быть
  как минимум три направления, в которых необходимо работать, чтобы
  приобретать клиентов.</p>

  <p align="justify">Работа с ранее продаваемыми объектами и хозяевами жилья
  занимает много времени и сил, тем не менее сделать это имеет
  смысл по трем причинам:</font></p>

  <p align="justify"><b>- Такие объекты легко находить.</b> Вам даже
  не нужно спрашивать владельцев, хотят ли они продать свое жилье.
  Достаточно работая с информацией и базой данных делать записи и
  пометки накапливая необходимую информацию. Это не такая уж
  сложная задача.</p>

  <p align="justify" ><b>- Такие объекты существуют при любом состоянии рынка.</b>
  Существует тенденция: Если рыночная ситуация
  такова, что продажи идут плохо, выставленные объекты продаются
  медленно, или вообще не продаются - срок выставления их на
  продажу в определенный момент истекает. Это дает неограниченные
  возможности получения новых объектов, которые можно продавать.
  Количество обращений продавцов в агентства постоянно
  увеличивается, так как их попытки продавать самостоятельно не
  увенчались успехом или специалист, который с ними работал, давно
  о них забыл.</p>

  <p align="justify">Если же рынок устойчив и продажи совершаются быстро - продавцы
  довольны этим темпом, соотношением выставленных на продажу и
  проданных объектов и радуются количеству предложений от
  покупателей. В такой ситуации увеличивается количество владельцев
  жилья, которые предпочитают обходиться без услуг агентства.
  Потребители считают, что продавать жилье очень просто и поэтому
  отказываются от услуг агентств.</p>

  <p align="justify">Поэтому агенты, правильно анализирующие состояние рынка могут
  регулировать количество продаж, смещая акценты в зависимости от
  состояния рынка:</font></p>

  <p align="justify">- Если продажи идут медленно - уделяйте больше внимания
  непродающимся объектам;</p>

  <p align="justify">- На устойчивом рынке – работайте больше с владельцами жилья;</p>

  <p align="justify"><b>Работа с такими объектами является самым лучшим обучением,
  которое может получить агент.</b> Если Вам в течение месяца удастся продать 2 или 3 ранее непродающихся
  объектов - считайте, что вы стали успешным ПРОДАВЦОМ.</p>

  <p align="justify"><b>Как  превратить плохой объект в хороший. Основы работы с безуспешно
  продававшимися объектами.</b></p>

  <p align="justify">Когда владелец объекта недвижимости и агент договариваются о
  сотрудничестве: - Заключается договор на продажу квартиры,
  который как правило заключается на срок не более месяца;</p>

  <p align="justify">- Либо это устная договоренность - срок в этом случае, как правило,
  не устанавливается, (как правило, это 2-3 месяца),тем не менее, существует некоторое время
  экспозиции (время активной рекламы объекта) в течение которого
  становится очевидным, насколько правильно оценен объект. Какой
  интерес к нему проявили потенциальные покупатели. Если в течение этого
  времени объект не продается, а агент и хозяин не договариваются о
  сроках продления отношений, то срок выставления этого объекта на
  продажу – истекает. Многие агенты даже не пытаются продлить
  отношения потому, что чувствуют себя неловко по отношению к
  владельцу. Ведь им не удалось совершить продажу в течение
  определенного времени и поэтому они считают, что хозяин не
  захочет и далее с ними работать. Чтобы избежать отказа агенты
  стараются не встречаться более с продавцом. И это ОШИБКА. Их
  пассивность приводит к тому, что другой агент получает
  возможность установить взаимоотношения с владельцем жилья и
  попытаться заработать на продаже его недвижимости.
  <i><u><b>Установление отношений (заключение
  договора) по объекту, который ранее не смогли продать является
  достаточно простым процессом, который многое агенты беспричинно
  усложняют.</b></u></i></p>

  <p align="justify">В целом, чтобы получить в свое распоряжение такой объект достаточно
  сделать один или несколько телефонных звонков. Избегайте почтовых
  и рассылок по электронной почте. Просто позвоните и назначьте
  встречу.</p>

  <p align="justify"><i><b>Считайте просроченный объект сделкой с высокой степенью
  вероятности.</b></i></p>

  <p align="justify">Имея в своем распоряжении информацию о продавце, Вы получаете в руки то, что
  называется <font color="#4F81BD"><i><b>сделкой высокой степени
  вероятности.</b></i></font> Существует много источников
  потенциальных клиентов:</br> 
  - Объявления в сети Интернет;</br>
  - рекламные объявления;</br>
  - звонки наудачу.</p>

  <p align="justify">Одни клиенты бывают
  - <i><b>возможными,</b></i> другие - <i><b>вероятными; </b></i>
  </p>

  <p align="justify">Разница между ними в следующем:</br>
  <i><b>- Возможные клиенты</b></i> – становятся реальными ориентировочно в половине
  случаев;</p>

  <p align="justify"><i><b>- Вероятные
  клиенты</b></i> - становятся реальными гораздо чаще;</p>

  <p align="justify">Работа с <i><b>вероятными клиентами</b></i> гораздо эффективнее для агента
  по недвижимости, а более вероятного клиента, чем владелец жилья с
  истекшим сроком выставления на продажу найти тяжело. Владелец уже
  продемонстрировал свое желание или потребность в совершении
  продажи и демонстрирует наличие проблемы в решении которой Вы
  можете помочь. Естественно проблема заключается в том, что в
  течение всего периода выставления на продажу объект так и не был
  продан. Однако большинство владельцев считают, что корни проблемы
  произрастают от нежелания или неумения агента по недвижимости
  надлежащим образом работать.<i><b> Хотя в 90% случаев реальной
  причиной по которой объект не продавался, являлась его завышенная
  цена.</b></i> В пятидесяти случаях такие объекты возвращаются на
  рынок с другими агентами. Почему бы не с Вами?</p>

  <p align="justify"><font color="#4F81BD"><b>Как получить
  просроченный объект.</b></font></p>

  <p align="justify">Работа с «просроченными» объектами недвижимости строится по принципу «все
  или ничего». Однако это направление не должно быть единственным в
  Вашей деятельности. Лучше всего его сделать одним из направлений.
  Не стоит работать по принципу «сегодня здесь, а завтра там».
  Либо Вы работаете с объектами с истекшим сроком продажи, причем
  на постоянной основе, либо Вы отказываетесь от этого направления
  деятельности. Не стоит «метаться из стороны в сторону» работать
  несколько дней с «просроченными» объектами, потом несколько
  недель заниматься чем-то другим, затем снова повернуть голову в
  исходное направление. Если Вы хотите заработать деньги на продаже
  уже выставлявшихся объектов, будьте готовы посвятить этой сфере
  часть своей деловой жизни.</p>

  <p align="justify">ШАГ № 1 - Начните
  работу с составления и списка «просроченных» объектов;</p>

  <p align="justify" >ШАГ № 2 - Установите телефоны и контакты, по которым можно связаться с
  владельцами; Используйте для этого базы данных, объявления,
  устаревшие объявления в газетах, интернете, записных книжках,
  объявления частных лиц и т.д.;</p>

  <p align="justify">ШАГ № 3 – Используя
  знания ценовой политики рынка, рыночной ситуации:
  - Рассортируйте объекты и установите причину, почему не удалось продать каждый из
  них. - Отдельно отложите информацию о тех объектах, вероятность
  продажи которых, на Ваш взгляд, была самой высокой.
 -Дополнительно  отложите информацию об объектах, которые располагались в
  интересующих Вас районах;</p>

  <p align="justify">ШАГ № 4 – Отрепетируйте заранее телефонные разговоры с владельцами жилья на
  основе ранее составленных сценариев и диалогов. Особое внимание
  уделите случаям когда со стороны владельца могут быть возражения.
  Знайте, что во время будущего телефонного разговора возможно
  перед Вами встанет задача преодоления многих препятствий и
  возражений и назначение личной встречи с владельцем недвижимости.
  Будьте готовы к тому, что владельцы разочарованы тем, что жилье
  не удалось продать. В большинстве случаев они винят в этом своего
  агента. С хозяевами непроданного жилья общаться достаточно
  сложно.</p>

  <p align="justify">ПОМНИТЕ – в большинстве случаев недовольство владельцев жилья направлено не в
  том направлении.<i><b> Реальная причина чаще всего заключается в
  цене, которую владелец назначил за свою недвижимость. Если Вы
  будете способствовать тому, что данный объект вернется на рынок и
  выставлен на продажу по такой же нереальной цене, то вы потерпите
  неудачу при попытке его продать.</b></i></p>

