  <p align="justify"><font color="#4F81BD"><b>ПОИСК ПОТЕНЦИАЛЬНЫХ ПРОДАВЦОВ И ПОКУПАТЕЛЕЙ</b></font></p>

  <p align="justify">Целью работы на перспективу является приобретение
  потенциальных клиентов. В сфере недвижимости этот процесс состоит
  из двух этапов.</p>

  <ol>
	<li>
	  <p align="justify"><b>Обнаружение и завоевание перспективных клиентов путем
	  установления контактов с людьми, которые заинтересованы в
	  ваших предложениях и имеют возможность стать вашими
	  клиентами.</b></p>
	</li>

	<li>
	  <p align="justify"><b>Назначение личных встреч в оговоренное время в
	  будущем.</b></p>
	</li>
  </ol>

  <p align="justify">Агенты по недвижимости осуществляют поиск клиентов двух
  категорий: продавцов, которые при предоставлении покупателя
  становятся вашими клиентами и покупателей, которые приобретают
  недвижимость.</p>

  <p align="justify"><font color="#4F81BD">ПОИСК ПОТЕНЦИАЛЬНЫХ ПРОДАВЦОВ.</font></p>

  <p align="justify">Сделки по продаже возникают в результате работы с прошлыми
  клиентами, людьми, находящимися в сфере вашего влияния, с помощью
  убеждения владельцев недвижимости сотрудничать с вами, путем
  обзвона продавцов. Главное то, что продажи редко происходят
  спонтанно, не требуя никаких усилий и предварительной работы.
  Происходит это по одной простой причине. Как правило, клиенты по
  рекомендации (направленные к вам текущим или прошлым клиентом)
  являются потенциальными покупателями. Принято считать, что агенты
  по недвижимости продают объекты просто разъезжая с клиентами по
  окрестностям и выбирают нужное им жилье.</p>

  <p align="justify">Чтобы получить клиента-продавца, необходимо провести
  упреждающую работу.</p>

  <p align="justify"><b>- Используйте все свои возможности по установлению деловых
  контактов;</b></font></p>

  <p align="justify"><b>-
  Собирайте информацию об объектах, у которых истек срок
  выставления на продажу или владельцы ранее предпочитали
  обходиться без услуг агентов;</b></p>

  <p align="justify"><b>- Установите приоритетность своих действий;</b>
  Необходимо установить приоритетность действий и
  затрачиваемого на них времени, в зависимости от вероятности
  достижения успеха. Некоторым потенциальным клиентам требуется
  уделить больше времени и ресурсов из-за необходимости уложиться
  со сделкой в короткие сроки, более высокого уровня обязательств
  перед ними или из-за большей величины комиссионных, которые может
  принести данная сделка.</p>

   <p align="justify"><font color="#4F81BD">ПОИСК ПОТЕНЦИАЛЬНЫХ ПОКУПАТЕЛЕЙ.</font></p>

  <p align="justify">Поиск потенциальных покупателей происходит легче, чем поиск
  продавцов. Это связано с тем , что клиенты – покупатели чаще
  приходят по рекомендации . Если Вы испытываете недостаток в
  потенциальных покупателях - увеличьте количество открытых показов
  (размещение продаваемых по реальной рыночной стоимости
  привлекательных ликвидных объектов на рекламном сайте в сети
  Интернет с демонстрацией фотографий объекта) и в рекламной
  газете. Категории демонстрируемых объектов определяют круг
  потенциальных покупателей. Очевидно, что престижные и
  дорогостоящие объекты привлекают более состоятельных клиентов,
  тогда как недорогое жилье является целевым объектом для менее
  богатых покупателей.</p>

  <p align="justify">Для быстрого развития успеха уделяйте больше внимания клиентам,
  которые хотят совершить покупку впервые. Проводите больше
  открытых показов. Польза от сотрудничества с первичными
  покупателями заключается в следующем:</p>

  <p align="justify"><b>- Они хотят быстро совершить
  покупку.</b> Для этого им не требуется
  продавать свое жилье для того , чтобы покупка стала
  возможной.</p>

  <p align="justify"><b>- Недостаточно опыта работы с другими агентами.</b>
 Первичные покупатели не находятся в сфере влияния
  других агентов и на них не давит какой-либо отрицательный опыт
  предыдущего сотрудничества с кем-то из ваших коллег.</p>

  <p align="justify"><b>- Если оказание услуги будет качественной - вы приобретаете верного
  клиента.</b>Это позволит установить
  взаимоотношения и в будущем этот человек может несколько раз
  продавать и покупать через Вас жилье.</p>

  <p align="justify"><b>- Они дадут Вам возможность установления контактов со своими
  друзьями,</b>которые могут намереваться
  так же купить и продать жилье.</p>

  <p align="justify"><font color="#4F81BD"><b>ЧЕТЫРЕ ВАЖНЫХ ПРАВИЛА РАБОТЫ НА ПЕРСПЕКТИВУ</b></font></p>

  <p align="justify"><i><b>1. Выработайте привычку ежедневно выделять время на поиск
	  потенциальных клиентов.</b></i></p>
	  
  <p align="justify" style="margin-left: 0.11cm; margin-bottom: 0cm"><i><b>2. Не обращайте
  внимания на отвлекающие моменты.</b></i> Большинство агентов в
  процессе работы отвлекаются на разные действия и, как правило,
  игнорируют свои обязательства по поиску потенциальных клиентов.
  Телефонные звонки, обсуждение проблем с коллегами, решение
  вопросов о демонстрации объектов, написание ответов на
  электронную почту и так далее - все это способствует рассеянию
  концентрации. Такая тактика называется <font color="#4F81BD"><i><b>творческим уклонением,</b></i></font> и агенты
  преуспели в ее освоении. Вне зависимости , являетесь ли Вы
  начинающим или одним из лучших агентов своей компании,
  отвлекающие факторы найдутся всегда. Фактически у лучших агентов,
  есть гораздо больше причин отвлекаться, поскольку на их плечах
  лежит больший объем работы, они обладают большим списком
  клиентов, имеют больший объем ответственности и консультируют
  своих менее опытных коллег. Разница между уклонением и успехом
  работы на перспективу сводится к одному вопросу: «Что Вы делаете,
  когда возникает отвлекающее обстоятельство?</p>

  <p align="justify" style=
  "margin-left: 0.11cm; margin-bottom: 0cm">- Откладываете ли Вы работу на перспективу до решения других
  вопросов?</p>

  <p align="justify" style=
  "margin-left: 0.11cm; margin-bottom: 0cm">- Предпочитаете ли заняться возникшим вопросом и сделать всего лишь
  несколько телефонных звонков?</p>

  <p align="justify" style= "margin-left: 0.11cm; margin-bottom: 0cm">Если на большинство
  этих вопросов Вы ответили утвердительно, вы исповедуете методику
  творческого уклонения. Чтобы успешно бороться с отвлекающими
  событиями, необходимо воспрепятствовать их возникновению.</p>

  <p>- Отключите электронную почту,
  чтобы Вас не отвлекали поступающие сообщения;</p>

  <p>- Попросите секретаря не
  переадресовывать Вам звонки носящий личный и не важный
  характер;</p>

  <p>- Отключите мобильный телефон, если
  он не работает на прием рекламных объявлений ;</p>

  <p>- Не принимайте предложений
  прерваться на чашку чая или сигарету;</p>

  <p>- Не вступайте в обсуждения
  повседневных новостей или домашних событий с теми, кто желал бы с
  Вами это обсудить в данный момент;</p>

  <p>- Если кто-то попросит Вас о
  встрече и по степени определения приоритетности она не носит
  важного характера - откажите. Скажите , что у вас назначена
  встреча . И это будет правдой , потому , что Вы как раз и
  занимаетесь организацией встреч с потенциальными клиентами.</p>

  <p><i><b>3. Выполняйте составленный
  план.</b></i></p>

  <p>Достижение успеха сводится к тому,
  чтобы совершить нужные действия в правильном порядке. Чтобы
  действия по поиску потенциальных клиентов проводились в
  правильном порядке необходимо работать и выполнять перспективный
  план. Вы должны знать, кому и по какой причине необходимо делать
  телефонные звонки сегодня, завтра, послезавтра и в последующие
  дни. Лучше всего заблаговременно составлять план работы на каждый
  день.</p>

  <p>Если составить план работы на
  начальном этапе поиска потенциальных клиентов, весьма вероятно,
  что вы запланируете сделать гораздо больше телефонных звонков,
  чем сделаете фактически. Мыслительный процесс будет все время
  стремиться увести Вас в сторону от конкретных действий. В голове
  будут возникать мысли, вроде: "Этот человек, наверное, подумает,
  что я беспокою его слишком часто" или «Наверное, этот человек не
  хочет продавать или покупать прямо сейчас».</p>

  <p>Если заранее составить план работы,
  вы будете готовы действовать, а не предугадывать. Чтобы достичь
  успеха в поиске потенциальных клиентов, поступайте так.</p>

  <p style="margin-left: 1.27cm;"><b>а)
  Проведите исследования, составьте план и наметьте действия по
  поиску потенциальных клиентов накануне.</b> Прежде чем уйти с
  работы, наметьте, какие телефонные звонки по поиску потенциальных
  клиентов вам необходимо будет сделать на следующий день. Соберите
  материалы, необходимые для этих звонков и положите их на свой
  рабочий стол. Когда на следующий день вы придете в офис, они
  сразу же привлекут Ваше внимание.</p>

  <p style="margin-left: 1.27cm;"><b>б) Утром
  следующего дня быстро просмотрите перечень намеченных звонков и
  задач на текущий день.</b></p>

  <p style="margin-left: 1.27cm;">Совет:
  Просматривайте план работы не слишком долго, иначе вы можете
  непроизвольно настроиться на тактику творческого уклонения.</p>

  <p style="margin-left: 1.27cm;"><b>в) Прежде,
  чем приступить к процессу работы на перспективу, потратьте 20
  минут на составление сценариев телефонных разговоров, репетицию
  диалогов и составление вариантов ведения беседы в случае, если
  клиенты будут высказывать возражения.</b></p>

  <p><b>4. Будьте искренни с самим собой
  и завершайте начатое.</b> Не уклоняйтесь от поставленных на день
  задач, сводя свои действия по поиску потенциальных клиентов .</p>

